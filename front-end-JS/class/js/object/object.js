



const obj = {
    name: "chaitanya",
    age: "25"
};                                    //here we can get the object properties in two ways
//here we are calling  the object properties with dot notation
console.log("using DOT Notation"+obj.name);                // 1) dot(.) notation  
console.log("using DOT Notation"+obj.age);                 // 2) square bracket notation
//here we are calling  the object properties with square bracket notation
console.log("using square bracket Notation"+obj["name"]);
console.log("using square bracket Notation"+obj["age"]);




